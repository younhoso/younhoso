1개월 여간(총 26회) 출연하여 아프리카사람들과 특히 어린이들의 고통 받는 실상을 영상을 통해 증언</p>
<p>  굶주림으로 죽어가는 말라위 기아들과 잠비아에서 만난 어느 한국인선교사부부가 고아원을 짓기 위해 바위를 손으로 잘게 쪼아다가 손가락이 다 뭉개지고 모잠비크의 어린 매춘부들이         매춘을 하면 에이즈에 걸려 죽는 줄 알면서도 굶지 않고 살기 위해서 죽음의 길을 걷는 어린소녀들을 위해 ‘세상의 아침’ 스튜디오에서 그들을 돕기 위해 방송을 통해 호소해 많은 시청         자들에게 사랑의 손길을 펼치게 했다</p>
</dd>
