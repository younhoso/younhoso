$(document).ready(function(){
    $(".tab_item").on('click', function(){
        const panel = $(this).data('target');

        $('.tab_item').removeClass('on');
        $(this).addClass('on');
        

        $('.tab_panel').removeClass('on');
        $(`#${panel}`).addClass('on');
    });

    $("#lst_more_btn").on('click', function(){
        const page = $(".page_more input").val();
        $(".page_more input").val(Number(page) + 1);

        $(`.lst_book.p_${page}`).css('display', 'flex');
    })
});

// 페이지 로드 시 해시에 따라 탭 활성화
document.addEventListener("DOMContentLoaded", (event) => {
    var currentTab = location.hash ? window.location.hash.replace('#', '') : window.location.hash = 'tab1';
    activateTab(currentTab);
});

function activateTab(tabId) {
  // 탭 활성화 로직 구현
  $('.nav_tab li button').removeClass('on');
  $(`.nav_tab .${tabId} button`).addClass('on');

  const n = tabId.replace('tab', '');
  $('.sub_cont .tab_panel').hide();
  $(`.sub_cont .tab${n}`).show();
}

function fnTabPub(n){
    $('.nav_tab li button').removeClass('on');
    $(`.nav_tab .tab${n} button`).addClass('on');

    $('.sub_cont .tab_panel').hide();
    $(`.sub_cont .tab${n}`).show();

    // URL의 해시를 업데이트합니다.
    window.location.hash = 'tab' + n;
}
</script>