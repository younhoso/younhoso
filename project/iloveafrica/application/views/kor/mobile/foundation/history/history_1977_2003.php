	<h3>1977년</h3>
	<dl>
		<dt>12월</dt>
		<dd><p class="f_bold">이창옥, 서부아프리카 라이베리아 몬로비아에 거주하다</p>
			<p>풍토병과 말라리아, 심한 눈병에 걸려 사경을 헤매게 되다. <br>
			죽지 않고 살아난 이창옥은 뭔가는 모르겠지만 아프리카 사람들을 위해 할일을 해야겠다는 생각을 갖게 된다. <br>
			자신도 모르게 티 없이 맑은 아프리카아이들이 가슴에 들어와 있는지도 모른 채 이창옥은 1979년 12월에 귀국하였다. </p>
		</dd>
