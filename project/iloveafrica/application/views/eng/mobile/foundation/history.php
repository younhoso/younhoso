<p>지가 Jigger 퇴치 캠페인</p>
<br>
<p><b>Where Jigger attack</b></p>
<b>진드기가 공격하는 부위</b><p></p>
<p><b>손. 발. 무릎. 음부. 하복부.</b></p>
<br>
<p><b>About Jiggers</b></p>
<p><b>아프리카의 진드기에 대해
</b></p>
<p>1. <b>Jiggers</b> 작은 핀의 머리둘레만한 사이즈의 벼룩으로 모래로 뒤덮인 훈훈한 지형이나 건조기후 지역에서 서식한다.</p>
<p>2. <b>Jiggers</b> 사막이나 해변, 외양간, 농장, 그리고 농장 주변의 토양이나 먼지 등에 더 많이 서식 해 있다.</p>
<p>3. <b>Jiggers</b> 가축 털의 사이, 바닥의 갈라진 틈, 거주지의 벽이나 가구와 같은 곳에 서식한다.</p>
<p>4. <b>Jiggers</b> 사람이나 고양이, 개, 쥐, 돼지, 소와 양과 같은 온혈 동물의 피를 먹고 살아갑니다.</p>
<p><b>Causes of Jigger infestation</b></p>
<p><b>진드기 체내침입의 원인</b></p>
<p>가난한 사람들은 비위생적인 환경과 비위생적인 주거지역에서 살아간다.</p>
<br>
<p><b>How it causes disease</b></p>
<p><b>진드기는 어떻게 발병되나</b></p>
<p>1. 임신한 암컷 진드기가 발가락이나 손톱 사이로 파고들어 알을 까면서 고통을 유발 시킨다.</p>
<p>2. 그렇게 몸속으로 침투한 진드기의 알이 커지고 모양이 완두콩 만하게 둥글게 형성된다. (지름이 약 1cm)</p>
<p>3. 이렇게 알이 배출이 되면 진드기는 땅에 떨어져 죽게 되고 알은 천천히 숙주의 피부로부터 껍질을 벗는다. </p> 
<p>4. 이것은 2차 감염이나 염증, 궤양, 섬유증, 임파선염, 	괴저, 발톱손실, 손가락 등의 자동절단과 같은   또 다른 감염을 유발하게 되고 심할 경우 죽음에 이르기도 한다. </p>
<br>
<p><b>Preventative Measures</b></p>
<p><b>예방책</b></p>
<p>1. 진드기에 감염된 환자의 집을 훈증소독 해야 한다.</p>
<p>2. 동물들로부터 진드기가 옮게 되는 경우가 많으므로 사람의 거주지역과 동물들이 사는 외양간이나 우리를 분리해야 한다.</p>
<p>3. 진드기에 감염된 환자와 그의 가족들을 위생 교육해야 한다.</p>
<p>4. 신발을 신겨야 한다.</p>
<p>5. 진드기에 감염되기 쉬운 환경에 노출된 사람들은 매일 자신들의 발을 체크하고 가려운 증상이나 위험증상이 없는지 살펴보며 항상 깨끗하고 청결하게 관리해야 한다. </p>
<p>6. 방충제를 피부에 뿌리고 만약 맨발로 더러운 바닥을 디뎠다면 빨리 방충제로 이물질을 제거해야 한다.</p>
<p>7. 진드기가 서식해 있는 지역이나 주변지역을 살포하고 소각소독하거나 살충제로 진드기를 죽이는 것도 예방책의 한 방법이다.</p> </dd>

