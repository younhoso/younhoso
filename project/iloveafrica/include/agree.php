제 12조 회원의 의무

1. 회원은 본 약관에 규정하는 사항과 기타 아이러브아프리카가 정한 제반 규정, 서비스 이용안내 또는 공지사항 등 아이러브아프리카가 공지 또는 통지하는 사항 및 관계법령을 준수하여야 하며, 기타 아이러브아프리카의 업무에 방해가 되는 행위, 아이러브아프리카의 명예를 손상 시키는 행위를 하여서는 안됩니다.

2. 아이러브아프리카가 관계법령 및 개인정보취급방침에 의하여 그 책임을 지는 경우를 제외하고 제11조의 관리소홀, 부정사용에 의하여 발생되는 모든 결과에 대한 책임은 회원에게 있습니다. 

3. 회원은 아이러브아프리카의 사전승낙 없이 서비스를 이용하여 영업활동을 할 수 없으며, 회원 간 또는 회원과 제3자 간에 서비스를 매개로 한 물품거래 및 서비스의 이용과 관련하여 기대하는 이익 등에 관하여 발생한 손해에 대하여 아이러브아프리카는 책임을 지지 않습니다. 이와 같은 영업활동으로 아이러브아프리카가 손해를 입은 경우 회원은 아이러브아프리카에 대하여 손해배상의 의무를 지며, 아이러브아프리카는 해당 회원에 대해 서비스 이용제한 및 적법한 절차를 거쳐 손해배상 등을 청구할 수 있습니다. 

4. 회원은 아이러브아프리카의 명시적인 동의가 없는 한 서비스의 이용권한, 기타 이용계약 상의 지위를 타인에게 양도, 증여할 수 없으며, 이를 담보로 제공할 수 없습니다. 

5. 회원은 서비스 이용과 관련하여 제22조 제1항의 각 호에 해당하는 행위를 하여서는 안됩니다. 

6. 이용자는 회원가입 신청 또는 회원정보 변경 시 실명으로 모든 사항을 사실에 근거하여 작성하여야 하며, 허위 또는 타인의 정보를 등록할 경우 일체의 권리를 주장할 수 없습니다. 

7. 회원은 주소, 연락처, 전자우편 주소(이메일과 동일함) 등 이용계약사항이 변경된 경우에 해당 절차를 거쳐 이를 아이러브아프리카에 즉시 알려야 합니다. 

8. 회원은 아이러브아프리카 및 제3자의 지적 재산권을 침해해서는 안됩니다. 

9. 회원은 다음 각 호에 해당하는 행위를 하여서는 안되며, 해당 행위를 하는 경우에 아이러브아프리카는 회원의 서비스 이용제한 및 적법 조치를 포함한 제재를 가할 수 있습니다. 
① 회원가입 신청 또는 회원정보 변경 시 허위내용을 등록하는 행위
② 다른 이용자의 ID, 비밀번호, 주민등록번호를 도용하는 행위
③ 이용자 ID를 타인과 거래하는 행위
④ 아이러브아프리카의 운영진, 직원 또는 관계자를 사칭하는 행위
⑤ 아이러브아프리카로부터 특별한 권리를 부여 받지 않고 아이러브아프리카의 클라이언트 프로그램을 변경하거나, 아이러브아프리카의 서버를 해킹하거나, 웹사이트 또는 게시된 정보의 일부분 또는 전체를 임의로 변경하는 행위
⑥ 서비스에 위해를 가하거나 고의로 방해하는 행위
⑦ 본 서비스를 통해 얻은 정보를 아이러브아프리카의 사전 승낙 없이 서비스 이용 외의 목적으로 복제하거나, 이를 출판 및 방송 등에 사용하거나, 제 3자에게 제공하는 행위
⑧ 아이러브아프리카 또는 제3자의 저작권 등 기타 지적재산권을 침해하는 내용을 전송, 게시, 전자우편 또는 기타의 방법으로 타인에게 유포하는 경우
