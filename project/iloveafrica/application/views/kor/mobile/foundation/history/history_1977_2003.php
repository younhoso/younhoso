	<dl>
		<dt>08월</dt>
		<dd><p class="f_bold">이창옥 부총재 
			"월드미스유니버시티=세계여자대학생평화봉사사절단" 
			KBS TV 방송사상 최초 아프리카대탐험 3만km 육로종단 도전</p>
			<p>KBS 생방송 “세상의 아침” 발대식을 마치고 8개국 자동차종단 탐험대원으로 도전(남아프리카공화국, 스와질랜드, 짐바브웨, 잠비아, 말라위, 모잠비크, 탄자니아, 케냐) 
			아프리카의 성자 ‘리빙스턴의 발자취를 찾아서 자동차로 국경을 넘고 넘어 남부에서 동부까지 오지를 돌아보며 아프리카의 실상을 체험하며 가슴에 멍이 들어간다.  </p>
			<p>가뭄으로 메말라 갈라진 붉은 땅에서 굶주림에 울부짖는 아이들, 나무껍질을 주어 씹으며 허기를 달래보는 눈물 젖은 아이, 에이즈로 죽어가는 아이, 영양실조로 말라버린 엄마의 젖꼭지를 물고 기운이 없어 울음조차 나오지 못하는 어린 생명, 허물어져 가는 병원이라는 곳에서 생명이 멈기 전 숨을 할딱이는 뼈만 남은 아이물 한 방울이 없어 씻지 못해 악취가 진동하는 아      이들... 	  </p>
			<p> 충격적인 가슴 아픈 아프리카 아이들을 가는 곳곳마다 접하면서 하나님께서 아프리카로 부르시고 보여주시는 숙제가 바로 고통 받는 아프리카아이들을 위해 뭔가 할 수 있는 일을 하라는 것이 아닌가 생각하며 탐험기간 내내 기도하며 그 일이 무엇일까!? 달리는 차안에서도 깊이 묵상한다. </p>
