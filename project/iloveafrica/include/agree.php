⑩ 모욕적이거나 개인신상에 대한 내용이어서 타인의 명예나 프라이버시를 침해할 수 있는 내용을 전송, 게시, 전자우편 또는 기타의 방법으로 타인에게 유포하는 행위
⑪ 다른 이용자를 희롱 또는 위협하거나, 특정 이용자에게 지속적으로 고통 또는 불편을 주는 행위
⑫ 아이러브아프리카의 승인을 받지 않고 다른 사용자의 개인정보를 수집 또는 저장하는 행위
⑬ 범죄와 결부된다고 객관적으로 판단되는 행위
⑭ 본 약관을 포함하여 기타 아이러브아프리카가 정한 제반 규정 또는 이용 조건을 위반하는 행위
⑮ 기타 관계법령에 위배되는 행위 


제 4장 서비스 이용
 
제 13조 서비스의 제공 및 변경

1. 아이러브아프리카는 아이러브아프리카가 제공하는 서비스에서 진행하는 컨텐츠와 이벤트 등의 모든 서비스를 회원에게 제공합니다.

2. 아이러브아프리카에서 제공하는 서비스는 기본적으로 무료입니다. 유료서비스 추가 시 사전 공지하며 이용에 대한 사항은 아이러브아프리카가 별도로 정한 서비스 약관 및 정책 또는 운영규칙에 따릅니다.

3. 아이러브아프리카는 서비스 변경 시 그 변경될 서비스의 내용 및 제공일자를 제 14조에 정한 방법으로 회원에게 통지합니다.


제 14조 정보의 제공 및 통지

1. 아이러브아프리카는 회원이 서비스 이용 중 필요하다고 인정되는 다양한 정보를 공지사항 혹은 전자우편 등의 방법으로 회원에게 제공할 수 있습니다.

2. 아이러브아프리카는 불특정다수 회원에 대한 통지를 하는 경우 7일 이상 공지 게시판에 게시함으로써 개별 통지에 갈음할 수 있습니다.


제 15조 게시물의 저작권 및 관리

1. 아이러브아프리카는 회원의 게시물을 소중하게 생각하며 변조, 훼손, 삭제되지 않도록 최선을 다하여 보호합니다. 
다만, 다음 각 호에 해당하는 게시물이나 자료의 경우 사전통지 없이 삭제하거나 이동 또는 등록 거부를 할 수 있으며, 해당 회원의 자격을 제한, 정지 또는 상실시킬 수 있습니다. 
① 다른 회원 또는 제3자에게 심한 모욕을 주거나 명예를 손상시키는 내용인 경우 
② 공공질서 및 미풍양속에 위반되는 내용을 유포하거나 링크시키는 경우 
③ 불법복제 또는 해킹을 조장하는 내용인 경우 
④ 영리를 목적으로 하는 광고일 경우 
⑤ 범죄적 행위에 결부된다고 인정되는 내용인 경우 
⑥ 아이러브아프리카가나 다른 회원의 저작권 혹은 제3자의 저작권 등 기타 권리를 침해하는 내용인 경우 
⑦ 아이러브아프리카에서 규정한 게시물 원칙에 어긋나거나, 게시판 성격에 부합하지 않는 경우 
⑧ 스팸 성향의 게시물인 경우 
⑨ 기타 관계법령에 위배된다고 판단되는 경우 

2. 아이러브아프리카가 작성한 저작물에 대한 저작권 및 기타 지적재산권은 아이러브아프리카에 귀속됩니다. 

3. 회원이 서비스 화면 내에 게시한 게시물의 저작권은 게시한 회원에게 귀속됩니다. 또한 아이러브아프리카는 게시자의 동의 없이 게시물을 상업적으로 이용할 수 없습니다. 다만, 비영리 목적인 경우는 그러하지 아니하며, 또한 본 사이트와 아이러브아프리카가 발행하는 우편물, 간행물 등 아프리카의 업무영역 내에서의 게재권을 갖습니다. 

4. 회원은 서비스를 이용하여 취득한 정보를 아이러브아프리카의 사전 승낙 없이 임의가공, 판매, 복제, 송신, 출판, 배포, 방송 기타 방법에 의하여 영리목적으로 이용하거나 제3자에게 이용하게 하여서는 안됩니다. 


제 16조 광고게재 및 광고주와의 거래

1. 아이러브아프리카가 제공하는 서비스 내에 다양한 배너와 링크(Link)를 포함할 수 있으며, 이는 광고주와의 계약관계에 의하거나 제공받은 컨텐츠의 출처를 밝히기 위한 조치입니다. 회원은 서비스 이용 시 노출되는 광고게재에 대해 동의합니다.

2. 서비스에 게재되어 있거나 본 서비스를 통한 광고주의 판촉 활동에 회원이 참여하거나 교신 또는 거래의 결과로서 발생하는 모든 손실 또는 손해에 대해 책임지지 않습니다.

3. 서비스 내에 포함되어 있는 링크를 클릭하여 타 사이트의 페이지로 옮겨갈 경우 해당 사이트의 개인정보취급방침은 아이러브아프리카와 무관합니다.


제 17조 서비스 이용시간

1. 서비스 이용은 아이러브아프리카의 업무상 또는 기술상 특별한 지장이 없는 한 연중무휴, 1일 24시간 운영을 원칙으로 합니다. 다만, 아이러브아프리카는 시스템 정기점검, 증설 및 교체를 위해 아이러브아프리카가 정한 날이나 시간에 서비스를 일시 중단할 수 있으며, 예정되어 있는 작업으로 인한 서비스 일시 중단은 아이러브아프리카가 제공하는 서비스를 통해 사전에 공지합니다. 

2. 아이러브아프리카는 서비스를 일정 범위로 분할하여 각 범위 별로 이용가능시간을 별도로 지정할 수 있습니다. 다만, 이 경우 그 내용을 공지합니다.


제 18조 서비스제공의 중단 등

1. 아이러브아프리카는 다음 각 호의 내용에 해당하는 경우 서비스 제공의 일부 혹은 전부를 제한하거나 중단할 수 있습니다.

① 정보통신설비의 보수 점검, 교체 및 고장 등 공사로 인한 부득이 한 경우
② 기간통신사업자가 전기통신서비스를 중단했을 경우
③ 서비스 이용의 폭주 등으로 정상적인 서비스 이용에 지장이 있는 경우
④ 국가비상사태 등 기타 불가항력적인 사유가 있는 경우

2. 전항에 의한 서비스 중단의 경우에는 아이러브아프리카는 제14조에 정한 방법으로 그 사유 및 기간 등을 공지합니다.
다만, 아이러브아프리카가 통제할 수 없는 사유로 발생한 서비스의 중단(시스템관리자의 고의, 과실 없는 디스크 장애, 시스템 다운 등)에 대하여 사전공지가 불가능한 경우에는 예외로 합니다.


제 19조 회원 ID 관리

1. 회원 ID와 비밀번호에 관한 모든 관리책임은 회원에게 있습니다.

2. 아이러브아프리카는 회원 ID에 의하여 제반 회원 관리업무를 수행하므로 회원이 ID를 변경하고자 하는 경우 아이러브아프리카가 인정할 만한 사유가 없는 한 ID의 변경을 제한할 수 있습니다.

