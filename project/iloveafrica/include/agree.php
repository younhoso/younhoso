제 1장 총칙
 
제 1조 목적

본 약관은 사단법인 아이러브아프리카(이하 "아이러브아프리카"이라 한다)가 운영하는 아이러브아프리카 홈페이지(www.iloveafrica.or.kr)에서 제공하는 인터넷관련서비스(이하 합하여 "서비스"라 한다)를 이용함에 있어 아이러브아프리카와 이용자의 권리, 의무 및 책임사항을 규정함을 목적으로 합니다. 

제 2조 용어의 정리

1. 본 약관에서 사용하는 용어의 정의는 다음과 같습니다.

'아이러브아프리카 홈페이지란 아이러브아프리카가 이용자에게 서비스를 제공하기 위하여 컴퓨터 등 정보통신설비를 이용하여 구성한 가상의 공간을 의미하며, 서비스 자체를 의미하기도 합니다.

'홈페이지회원'이란 아이러브아프리카에 개인정보를 제공하여 회원등록을 한 자로서, 아이러브아프리카와 서비스 이용 계약을 체결하고 회원 아이디를 부여 받은 자를 의미합니다.

'후원회원'이란 아이러브아프리카의 사회복지사업에 참여하기 위해 후원금을 정기적 혹은 일시적으로 납부하는 자를 의미합니다.

'비회원'이란 아이러브아프리카의 홈페이지에 회원가입을 하지 않고 서비스를 이용하는 자를 의미합니다.

'이용계약'이란 서비스를 제공받기 위해 아이러브아프리카와 체결하는 계약을 의미합니다.

'회원 아이디(이하 "ID"라 한다)'란 회원의 식별과 회원의 서비스 이용을 위하여 회원이 선정하고 아이러브아프리카가 승인하는 문자 또는 숫자의 조합을 의미합니다.

'비밀번호'란 회원이 부여 받은 ID와 일치된 회원임을 확인하고, 회원의 개인정보를 보호하기 위하여 회원이 정한 문자와 숫자의 조합을 의미합니다. 

'메일회원'이란 아이러브아프리카에 성명과 e-Mail 주소만을 제공하여 회원등록을 하고 아이러브아프리카 홈페이지가 발송 하는 메일을 수신하는 것을 내용으로 하는 서비스 이용 계약을 아이러브아프리카와 체결한 자를 의미합니다. 단, 메일회원은 추가 가입 신청을 받거나 추가 회원 승인을 하지 않습니다.

'회원탈퇴(이하 "탈퇴"라 한다)'란 아이러브아프리카 또는 회원이 홈페이지 이용계약을 해지하는 것을 의미합니다.

2. 본 약관에서 사용하는 용어의 정의는 제1항에서 정하는 것을 제외하고는 관계법령 및 서비스 별 안내에서 정하는 바에 의합니다. 


제 3조 이용약관의 효력 및 변경

1. 아이러브아프리카는 본 약관의 내용을 회원이 쉽게 알 수 있도록 초기 서비스화면에 게시하거나, 초기서비스화면에서의 링크를 통해 제공합니다.

2. 아이러브아프리카는 약관의 규제에 관한 법률, 전자거래기본법, 전자서명법, 정보통신망 이용촉진 및 정보보호 등에 관한 법률 등 관련법을 위배하지 않는 범위에서 본 약관을 개정할 수 있습니다.

3. 아이러브아프리카는 본 약관을 개정할 경우에는 적용일자 및 개정사유를 명시하여 현행 약관과 함께 아이러브아프리카가 제공하는 서비스 사이트의 초기 화면에 그 적용일자 15일 이전부터 적용일자 전일까지 공지합니다. 다만, 회원에게 불리하게 약관내용을 변경하는 경우에는 최소한 30일 이상의 사전 유예기간을 두고 공지합니다. 이 경우 아이러브아프리카는 개정 전 내용과 개정 후 내용을 명확하게 비교하여 회원이 알기 쉽도록 표시합니다.

4. 회원은 개정된 약관에 대해 거부할 권리가 있습니다. 회원은 개정된 약관에 동의하지 않을 경우 서비스 이용을 중단하고 회원등록을 해지할 수 있습니다. 단, 개정된 약관의 효력발생일로부터 7일까지 거부의사를 표시하지 아니하고 서비스를 계속 이용할 경우 약관의 변경사항에 동의한 것으로 간주합니다.

5. 변경된 약관에 대한 정보를 알지 못해 발생하는 회원 피해는 아이러브아프리카가 책임지지 않습니다.


제 4조 약관 외 준칙

1. 본 약관은 아이러브아프리카가 제공하는 서비스에 관해 별도의 정책 및 운영규칙과 함께 적용됩니다.
2. 본 약관에 명시되지 아니한 사항과 본 약관의 해석에 관하여는 관계법령에 따릅니다. 
 
제 2장 이용계약 체결
 
제 5조 이용계약의 성립

1. 이용계약은 회원의 본 이용약관 내용에 대한 동의와 이용신청에 대하여 아이러브아프리카의 이용승낙으로 성립합니다.

2. 본 이용약관에 대한 동의는 회원등록 당시 본 약관을 읽고 "위 서비스 약관에 동의합니다"의 대화창에 표시를 한 후 등록하기 단추를 누름으로써 의사표시를 한 것으로 간주합니다.


제 6조 서비스 이용신청

1. 회원으로 가입하여 본 서비스를 이용하고자 하는 이용고객은 아이러브아프리카에서 요청하는 제반 정보
(이름, 주민등록번호, 연락처 등)를 제공하여야 합니다. 단, 이 때 회원이 제공한 제반 정보는 개인정보취급방침에 따라 관리됩니다. 

2. 회원은 반드시 아이러브아프리카가 제공하는 서비스가 요구하는 회원 본인의 실명 인증 절차를 거쳐야만 서비스를 이용할 수 있으며, 실명으로 등록하지 않은 회원은 일체의 권리를 주장할 수 없습니다. 단, 회원이 제공한 주민등록번호는 동명이인의 구분과 아이러브아프리카 후원회원의 관리 목적 외에는 그 어떠한 용도로도 사용되지 않습니다.

3. 회원가입은 반드시 실명으로만 가입할 수 있으며 아이러브아프리카는 실명확인조치를 할 수 있습니다. 

4. 타인의 명의(이름 및 주민등록번호)를 도용하여 이용신청을 한 회원의 모든 ID는 삭제되며, 관계법령에 따라 처벌을 받을 수 있습니다. 

5. 아이러브아프리카는 본 서비스를 이용하는 회원에 대하여 등급별로 구분하여 이용시간, 이용회수, 서비스 메뉴 등을 세분하여 이용에 차등을 둘 수 있습니다. 

6. 만14세 미만의 아동이 회원으로 가입하기 위해서는 반드시 개인정보의 수집 및 이용목적에 대하여 충분히 숙지하고 법정대리인(부모)의 동의를 받아야 합니다. 법정대리인의 허락을 받지 않은 14세 미만의 아동에 대해서는 회원에서 제외할 수 있습니다.


제 7조 개인정보의 보호 및 사용

1. 아이러브아프리카는 관계법령이 정하는 바에 따라 회원등록정보를 포함한 회원의 개인정보를 보호하기 위해 노력합니다. 
회원 개인 정보의 보호 및 사용에 대해서는 관련법령 및 아이러브아프리카의 개인정보 취급방침이 적용됩니다. 단, 아이러브아프리카의 공식사이트 이외의 웹에서 링크된 사이트에서는 아이러브아프리카의 개인정보취급방침이 적용되지 않습니다. 또한 아이러브아프리카는 회원의 귀책사유로 인해 노출된 정보에 대해서 일체의 책임을 지지 않습니다. 

2. 아이러브아프리카는 이용자에게 제공하는 서비스의 양적, 질적 향상을 위하여 이용자의 개인정보를 제휴사에게 제공, 공유할 수 있으며, 이 때에는 반드시 이용자의 동의를 받아 필요한 최소한의 정보를 제공, 공유하며, 개인정보취급방침에 누구에게 어떤 목적으로 어느 정도의 기간 동안 어떤 정보를 제공, 공유하는지 명시합니다.

3. 회원은 원하는 경우 언제든 아이러브아프리카에 제공한 개인정보의 수집과 이용에 대한 동의를 철회할 수 있으며, 동의의 철회는 회원 탈퇴를 하는 것으로 이루어집니다.


제 8조 이용신청의 승낙과 제한

1. 아이러브아프리카는 제 6조의 규정에 의한 이용신청고객에 대하여 업무 수행상 또는 기술상 지장이 없는 경우에 원칙적으로 접수순서에 따라 서비스 이용을 승낙합니다. 

2. 아이러브아프리카는 다음 각 호의 내용에 해당하는 경우 이용신청에 대한 승낙을 제한할 수 있고, 그 사유가 해소 될 때까지 승낙을 유보할 수 있습니다. 
① 아이러브아프리카의 서비스 관련설비에 여유가 없는 경우
② 아이러브아프리카의 기술상 지장이 있는 경우
③ 기타 아이러브아프리카의 사정상 필요하다고 인정되는 경우 

3. 아이러브아프리카는 다음 각 호의 내용에 해당하는 경우 이용신청에 대한 승낙을 하지 아니할 수도 있습니다. 
① 실명이 아니거나 타인의 명의를 이용하여 신청한 경우
② 이용계약 신청서의 내용을 허위로 기재한 경우
③ 사회의 안녕과 질서, 미풍양속을 저해할 목적으로 신청한 경우
④ 부정한 용도로 본 서비스를 이용하고자 하는 경우
⑤ 영리를 추구할 목적으로 본 서비스를 이용하고자 하는 경우
⑥ 기타 아이러브아프리카가 정한 등록신청 요건이 미비한 경우
⑦ 본 서비스와 경쟁관계가 있는 이용자가 신청하는 경우
⑧ 기타 규정한 제반사항을 위반하며 신청하는 경우 

4. 아이러브아프리카는 이용신청고객이 관계법령에서 규정하는 미성년자일 경우에 정보통신망이용촉진및정보보호등에관한법률" 등에 따라 법정 대리인의 동의가 없을 경우 승낙을 보류할 수 있습니다.


제 9조 회원 아이디 부여 및 변경 등

1. 아이러브아프리카는 회원에 대하여 본 약관에 정하는 바에 따라 회원 ID를 부여합니다. 

2. 회원 ID는 원칙적으로 변경이 불가하며 부득이한 사유로 인하여 변경 하고자 하는 경우에는 해당 ID를 해지하고 재가입해야 합니다.

3. 회원은 서비스 페이지 마이페이지(My Page) 또는 기타 해당 페이지로 링크된 메뉴를 통하여 자신의 개인정보를 관리할 수 있는 페이지를 열람할 수 있으며, 해당 페이지에서 언제든지 본인의 개인정보를 열람하고 수정할 수 있습니다. 다만, 서비스 관리를 위하여 반드시 필요한 실명, 회원 ID, 성별 등은 수정할 수 없습니다. 

4. 회원 ID는 다음 각 호에 해당하는 경우에 회원 또는 아이러브아프리카의 요청으로 변경할 수 있습니다.
① 회원 ID가 회원의 전화번호 또는 주민등록번호 등으로 등록되어 사생활 침해가 우려되는 경우
② 타인에게 혐오감을 주거나 미풍양속에 어긋나는 경우
③ 기타 합리적인 사유가 있는 경우 

5. 회원 ID 및 비밀번호의 관리책임은 회원에게 있습니다. 이를 소홀이 관리하여 발생하는 서비스 이용상의 손해 또는 제3자에 의한 부정이용 등에 대한 책임은 회원에게 있으며 아이러브아프리카는 아이러브아프리카의 고의 또는 중과실이 개입되어 있지 아니한 이상 그에 대한 책임을 일절 지지 않습니다.

 
제 3장 계약 당사자의 의무
 
제 10조 아이러브아프리카의 의무

1. 아이러브아프리카는 관련법과 본 약관이 금지하거나 공서양속에 반하는 행위를 하지 않으며, 본 약관이 정하는 바에 따라 지속적이고, 안정적으로 서비스를 제공하기 위하여 최선을 다하여야 합니다.

2. 아이러브아프리카는 회원이 안전하게 서비스를 이용할 수 있도록 회원의 개인정보보호를 위한 보안시스템을 구축하며 개인정보취급방침을 공시하고 준수합니다.

3. 아이러브아프리카는 수신거절의 의사를 명백히 표시한 회원에 대해서는 회원이 원하지 않는 영리목적의 광고성 전자우편 (이메일)을 발송하지 않습니다.

4. 아이러브아프리카는 이용계약의 체결, 계약사항의 변경 및 해지 등 회원과의 계약관계절차 및 내용 등에 있어 회원에게 편의를 제공하도록 노력합니다. 아이러브아프리카 웹마스터에게 회원이 탈퇴 신청 메일을 보낼 경우 즉시 탈퇴 처리를 합니다.

5. 아이러브아프리카는 이용고객으로부터 제기되는 의견이나 불만이 정당하다고 객관적으로 인정될 경우에는 적절한 절차를 거쳐 즉시 처리하여야 합니다. 다만, 즉시 처리가 곤란한 경우는 이용자에게 그 사유와 처리 일정을 통보하여야 합니다. 


제 11조 회원의 ID 및 비밀번호에 대한 의무

1. 아이러브아프리카가 관계법령 및 개인정보취급방침에 의하여 그 책임을 지는 경우를 제외하고, 회원 ID와 비밀번호의 관리책임은 회원에게 있습니다.

2. 회원은 자신의 ID 및 비밀번호를 제3자가 이용하게 해서는 안됩니다.

3. 회원이 자신의 ID 및 비밀번호를 도용 당하거나 제3자가 사용하고 있음을 인지한 경우에는 바로 아이러브아프리카에 통보하고 아이러브아프리카의 안내가 있는 경우에는 그에 따라야 합니다.

4. 아이러브아프리카는 회원이 상기 제1항, 제2항, 제3항을 위반하여 회원에게 발생한 손해에 대하여 책임을 일절 지지 않습니다.

